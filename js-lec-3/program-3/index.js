// Primitive data type (string)
let userName = "AbdulMoid"; 

// Non-primitive data type (object)
let userDetails = [
    userName = "AbdulMoid" ,
    age = 20 ,
    skill = "Frontend Developer"
];

// Print values with their types
console.log("Username:", userName, "| Type:", typeof userName);
console.log("User Details:", userDetails, "| Type:", typeof userDetails);
